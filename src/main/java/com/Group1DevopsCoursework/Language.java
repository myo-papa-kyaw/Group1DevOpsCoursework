package com.Group1DevopsCoursework;

/**
 * Represents a language and its global usage statistics.
 * Each Language object stores:
 * - The language name
 * - The estimated number of speakers worldwide
 * - The percentage of the world population that speaks it
 * Used in the language reporting section.
 */

public class Language {

    /** The name of the language */
    public String language;

    /** The total number of people who speak the language. */
    public long speakers;

    /** The percentage of the world population who speak the language. */
    public double percentage;
}
