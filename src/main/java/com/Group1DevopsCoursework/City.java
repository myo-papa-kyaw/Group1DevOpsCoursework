package com.Group1DevopsCoursework;

/**
 * Represents a city in the world database.
 * Each City object stores:
 * - The name of the city
 * - The country it belongs to
 * - The district
 * - The population of the city
 * This class is used when generating city-based reports.
 */

public class City {

    /** The name of the city. */
    public String name;

    /** The country in which the city is located. */
    public String country;

    /** The district or region that the city belongs to. */
    public String district;

    /** The population of the city. */
    public int population;
}
